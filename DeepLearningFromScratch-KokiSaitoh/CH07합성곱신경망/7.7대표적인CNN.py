"""
LeNet (1998년)
- 손글씨 숫자 인식을 위해 제안된 초창기 신경망 구조임
- 합성곱 계층과 서브샘플링(크기만 단순 축소하는 풀링) 계층을 반복하고, 완전연결 계층을 거쳐 결과를 출력함
- 현대 CNN과의 차이점: 활성화 함수로 시그모이드를 사용했으며, 최대 풀링(Max Pooling) 대신 서브샘플링을 사용함

AlexNet (2012년)
- 딥러닝 열풍을 점화시킨 구조로, 합성곱 계층과 풀링 계층을 반복하고 마지막에 완전연결 계층을 통과하는 기본 뼈대를 유지함
- LeNet의 기본 구조에서 벗어나지 않았으나 성능 향상을 위한 현대적 기법을 대거 도입함
- 주요 변경점: 활성화 함수로 ReLU를 사용함, LRN(국소적 정규화) 계층을 채택함, 과대적합을 억제하기 위해 드롭아웃(Dropout)을 활용함
"""